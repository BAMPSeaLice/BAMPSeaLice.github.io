<H2>BAMP 2011 field/laboratory activities and preliminary findings</H2>
<P>
	<UL>
		<LI>During the 2011 outmigration period (March - June), just under 65,000 fish (60,953 pink; 3,630 chum and 407 coho) 
			were captured at up to 104 individual sites, over four sampling trips.</LI>
		<LI>Where available, up to 30 fish per species was retained at each sampling location. This sub-set of just over 6,000 fish 
			(~70% pink, ~25% chum and ~4% coho) were lethally sampled and submitted to one of two laboratories for subsequent analysis.</LI>
		<LI>Overall a similar number of wild salmon were retained for analysis in the laboratories and in broadly similar proportions 
			across the species groups as was the case in 2010. This is despite the fact that 15,000 more fish were captured in 2011 
			(a total of 64,990 in comparison to 49,747 in 2010); due to significantly increased catch sizes, particularly of pink salmon, in Trip 2.</LI>
		<LI>As was noted in the 2010 dataset, there is a strong correlation between prevalence and abundance values for the lice infestation 
			patterns. Overall the prevalence of both lice species (<I>L. salmonis</I> and <I>C. clemensi</I>) on wild fish in 2011 was low, with the prevalence 
			of only <I>C. clemensi</I> reaching more than 10% in Trips 2 and 3.</LI>
		<LI>The average lice prevalence for both <I>L. salmonis</I> and <I>C. clemensi</I> on pink and chum salmon was significantly lower than that seen in 2010 
			across all four trips, though the temporal trends were similar with levels being lowest in March (1-2%), rising in April (3-15%) and 
			peaking in May (5-18%), before dropping off in June (2-5%).</LI>
		<LI>At all time points the levels of sea lice on chum were consistently a few percentage points higher than those seen on pink salmon. 
			In addition <I>C. clemensi</I> were the more prevalent lice species for all trips and across both wild host species; this contrasts with 2010 
			where infestation was more varied with <I>L. salmonis</I> having a similar prevalence.</LI>
		<LI>The coho salmon observed appeared to have infestation rates not dissimilar to those seen on the other host species for <I>L. salmonis</I>, 
			though for <I>C. clemensi</I> the prevalence values in Trip 2 (43%) and Trip 4 (19%) were higher than for other fish species. However, the small 
			sample sizes involved make it difficult to generalize and/or make definitive comments on temporal or spatial trends for coho.</LI>
		<LI>In general the chalimus/non-motile stages of lice made up the bulk of the lice infestation on pink and chum salmon - accounting for 
			almost 100% in the first two trips, around 60% in the third trip, but falling to around 10% in the fourth trip.</LI>
		<LI>Fish measurements from the lab indicated typical growth patterns, with length and weight trends similar to those observed in 2010. 
			Pink salmon grew from a mean of around 0.25g in March to just over 3g by the end of June; with chum following a similar pattern, 
			growing from 0.42g to just over 4g.</LI>
		<LI>The proportion of fish observed to have "scarring" (abrasions presumed to be due to sea lice feeding on the juvenile skin) in 2011 was 0.4%;
			much lower than the proportions recorded in 2010 (where mean levels of affected fish were 4.3% and 6%, for chum and pink respectively).</LI>
	</UL>
</P>

