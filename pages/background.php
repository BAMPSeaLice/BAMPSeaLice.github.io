<H2>background</H2>
<P>In 2008, <B>Marine Harvest Canada</B> (MHC) collaborated with the <B>Coastal Alliance for Aquaculture Reform</B> (CAAR)*
to develop a six year <B>Coordinated Area Management Plan</B> (CAMP) for MHC salmon farms in the Broughton Archipelago.</P>
<P>The CAMP established an area management approach (using timing and location of wild smolt entry, coordinated harvesting and fallowing, 
extensive sea lice monitoring, and therapeutic treatment) to reduce the potential for <a href = "faq.html#Q3">sea lice</a> from farmed salmon 
to infect wild juvenile pink and chum salmon during the out migration season (March 1st to June 30th) in 
the <a href = "../images/BA_map_1.png" target = "_blank">Tribune-Fife and lower Knight Inlet corridors.</A></P>
<P>CAMP was jointly monitored by MHC and CAAR in 2009.  Mainstream Canada and Grieg Seafood 
also entered into preliminary discussions with MHC, CAAR and the provincial government to participate in CAMP (renamed Broughton Area Plan).
These initial discussions were suspended with the jurisdictional shift from provincial to federal responsibility.  Production amendments required to fully 
implement CAMP (or the Broughton Area Plan) were never approved; hence out-migration corridors were only partially achieved.</P>
