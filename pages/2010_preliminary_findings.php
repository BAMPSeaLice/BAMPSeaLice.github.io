<H2>BAMP 2010 field/laboratory activities and preliminary findings</H2>
<P>
	<UL>
		<LI>During the 2010 spring outmigration period (March – June), approximately 50,000 juvenile salmon were captured in the Broughton Archipelago by a collaborative team of researchers
			 including personnel from Fisheries and Oceans Canada, the Coastal Alliance for Aquaculture Reform, the University of Alberta, the University of Otago, 
			 Grieg Seafoods, Mainstream Canada and Marine Harvest Canada.</LI>
		<LI>The majority of fish captured were pink salmon (95%), followed by chum salmon (5%) and coho salmon (0.7%). The weight and length of these fish increased during the season as expected.
			 Pink salmon increased from a mean of around 0.2g in April to just over 3g by the end of June; chum followed a similar pattern though were slightly larger throughout,
			  growing from 0.5g to just over 4g.</LI>
		<LI>In terms of temporal trends, about 95% of the pink and chum salmon were captured during the first three months of the monitoring program (March – May).  
			Chum salmon prevalence peaked in April (46% of the total capture), while pink salmon prevalence peaked in May (49 % of the total capture).  
			The numbers of coho salmon were too low to make clear generalizations.</LI>
		<LI>Nearly 7,000 fish were assessed in the field (5,190 pink, 1,470 chum and 288 coho) to determine lice levels (“live licing”).  
			A sub-set of these (just under 6,100) (4,587 pink, 1,393 chum and 110 coho) were euthanized and submitted to one of two laboratories for subsequent analysis.</LI>
		<LI>The program included detailed auditing and analysis of results to evaluate the quality and adequacy of data received from individuals documenting lice data in the field 
			(live licing) and from the two laboratories undertaking the lab based lice analyses (post-mortem licing).</LI>
		<LI>Observations of lice prevalence (all lice species and stages) in the field ranged from 10-30% on pink salmon, 16 -36 % on chum salmon and 35-72% on coho salmon.  
			The estimated average lice prevalence for pink and chum salmon live lice counts over all four trips was broadly similar at around 25-30%, with chum consistently a few 
			percentage points higher on each trip. In terms of temporal variation the levels of prevalence on pink-chum appear to be lowest in March (10-15%), 
			rising thereafter to peak in May (30-36%), with June showing similar levels to April in both the cases of pink (25%) and chum (28-32%) salmon.  </LI>
		<LI>The coho salmon live licing results exhibited a somewhat higher prevalence rate (35-72%), and correspondingly higher abundance/intensity. However, the relatively small sample
			 sizes make it difficult to generalize and/or make definitive comments on temporal or spatial trends.</LI>
		<LI>In general the chalimus/non-motile stages of lice made up the bulk of the lice infestation – accounting for over 80% in the first two trips, around 50% in the third trip, 
			and only falling to around 25% in the fourth trip for the case of pink salmon. The case of chum appears to be somewhat different from pink salmon in that the mobile stages 
			never accounted for more than around a quarter of the overall infestation on the chum, while by Trip 4 these stages accounted for almost 65% of the infection on the pink salmon.</LI>
		<LI>Both <I>L. salmonis</I> and <I>Caligus</I> species of lice were seen to be present on all salmon species throughout the season. In late March the proportion of 
			<I>Caligus</I> appeared to be higher (around 75% of total) while numbers for the rest of the season were broadly similar (with a possible increase in the 
			proportion of <I>L. salmonis</I> during the last sampling trip but only on chum salmon).</LI>
		<LI>Only a preliminary analysis has been completed to date with early indications suggesting that 2010 sea lice prevalence and abundance on juvenile salmon species are similar to those recorded
			over the past few years. A more thorough analysis is warranted following integration of the historical datasets.</LI>
	</UL>
</P>

