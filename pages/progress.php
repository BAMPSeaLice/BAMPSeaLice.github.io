<H2>progress</H2>
<P>
	<UL>
		<LI>Just prior to the development of BAMP, a workshop sponsored by MHC and CAAR (Novâ€™09)
			 reviewed recent research on the effects of lice on juvenile salmon.  Scientists from the Norwegian Institute for Nature Research, 
			 DFO, University of British Columbia, Simon Fraser University, University of Washington, CAAR and MHC reviewed knowledge gaps / areas of consensus; a summary report is forthcoming.</LI>
		<LI>Four 6 to 8-day wild salmon sampling events were completed between March and June in both 2010 and 2011.</LI>
		<LI>At each site, lethal samples were randomly divided in half and sent to two different laboratories (Salmon Coast Field Station, 
		Echo Bay and the BC Center for Aquatic Health Sciences, Campbell River) 
			for assessment under microscope.  Project specific laboratory quality assurance protocols were developed to ensure lice assessment data were audited on a real time basis.</LI>
		<LI>Weather stations were deployed at 9 locations throughout the archipelago to collect meteorological data that will be used to force a hydrodynamic ocean circulation model.</LI>
		<LI>Data Sharing Agreements were signed by all of the BAMP Science Team members (government, researchers and farming industry) in 2010, agreeing to share historical data sets.</LI>
		<LI>Preliminary population modeling was undertaken using 2010 sea lice data from wild and farmed salmon, in an attempt to quantify the magnitude and spatial extent of sea lice
			 transmission between salmon farms and wild juvenile salmon.</LI>
