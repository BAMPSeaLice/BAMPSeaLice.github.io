<H2>faq</H2>
<P><B>Q: what is the key motivation behind BAMP?</B></P>
 
<P>BAMP grew out of an area management project in the Broughton Archipelago intended to significantly reduce the likelihood that 
sea lice from farmed salmon would infect out-migrating juvenile wild salmon in the area. BAMP is intended to measure lice on farmed 
and wild fish and to assess the effectiveness of farm management actions.</P>
 
<P><B>Q: how are BAMP activities managed?</B></P>
 
<P>BAMP research activities are guided by a Science Team made up of a number of independent researchers. The broader operational 
governance of the program is the responsibility of these researchers working in tandem with representatives of the sponsoring 
organisations. In 2010 the BAMP Science Team established an initial set of research and monitoring protocols to support focused 
efforts to improve understanding of the interactions between farmed salmon, wild salmon and sea lice in the Broughton Archipelago. 
Both the research plan and the monitoring protocols are subject to annual review by the Science Team.</P>
 
<a name="Q3"></a>
<P><B>Q: what are sea lice?</B></P>
 
<P>Sea lice are small marine parasites that occur naturally on many different species of wild fish including wild adult salmon. 
	The early development stages of sea lice are planktonic and are transported on water currents and tides. When they encounter marine fish they attach themselves, usually on the skin, fins and/or gills 
	and feed off the mucous or skin. There are 13 known species of sea lice in the marine waters of British Columbia, but the common ‘salmon louse’ is the 
	one we hear the most about. The Latin name for this salmon louse is <I>Lepeophtheirus salmonis</I>. In the spring, when fish eggs hatch and juvenile 
	salmon migrate down the rivers and enter the ocean they may become infected with sea lice from salmon farms where they are located along 
	wild salmon migration routes.</P>
 
<P><B>Q: how many wild fish are sampled and from which sites?</B></P>
 
<P>Since 2010 the BAMP field team has been sampling wild Pacific salmon at around 100 sites across the Broughton Archipelago to measure sea lice 
levels. At each site up to 30 fish of the species of interest (pink, chum and coho) can be retained if they are present. Once the data from 2011 
are available for analysis an assessment will be made as to whether it might be possible to reduce the number of sites from which fish are sampled 
and if so, which sites.</P>

