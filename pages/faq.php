<P><B><I>Abundance</I></B></P>
<P>This measure simply describes the mean number of lice across <B>all</B> of the fish which were sampled - in this sense it is the closest to what we colloquially 
think of when we say, "the average". In the example above, for pink salmon we found a total of 28 <I>L. salmonis</I> lice on the 977 fish observed, and thus 
the abundance is simply 28 divided by 977, here rounded to 0.03 (the actual value is closer to 0.02866).</P>
<P><B><I>Intensity</I></B></P>
<P>One of the problems with a traditional 'average' is that it is of limited use when there are many individuals who return a value of zero - in our 
example there are in fact 951 fish on which there is no infestation.  The "intensity" measure also provides the mean number of lice, but <B>only</B> on 
those fish that are infested. In the example above, for pink salmon the 28 <I>L. salmonis</I> lice were 
found on 26 of the observed fish, and thus the intensity is simply 28 divided by 26, which we have rounded to the value of 1.1.    
We also report the range, the larger number in which indicates the maximum number of lice found on any one fish - in the case we have been looking at 2, 
or in the case of <I>C. clemensi</I> lice on chum salmon 5.  [The thoughtful reader may observe that if every fish was found to be infested then the intensity 
and abundance would be identical, however, as can be seen from the data reported here this rarely the case for lice on wild juvenile salmon.]</P>
<P><B><I>Prevalence</I></B></P>
<P>In cases where overall abundance is low and many fish have no infestation it is often informative to report the "prevalence" which indicates 
the proportion of fish on which there was at least one louse present. In the case of <I>L. salmonis</I> shown above, 26 of the pink salmon observed had at 
least one louse present out of the total of 977 fish examined. Dividing 26 by 977 gives us a value of 0.0266 which, after rounding, can be expressed 
as 2.7% prevalence.</P>
<P><B><I>95% Confidence Interval (CI)</I></B></P>
<P>Each of the three measures noted above are referred to as "point estimates" for the values estimated to be representative for the population of fish 
from which these samples were taken. There will of course be variability associated with these values and it is often useful to summarise the range of 
possible values that might be likely to arise. There are a number of ways to express this variability but all of these (apart from the simple "range" shown 
in the case of intensity) are reasonably complex from the point of view of statistical interpretation. We have chosen to show the "95% CI" associated 
with prevalence values which for the 2.7% result for <I>L. salmonis</I> on pink salmon is expressed as [1.7 - 3.7]. This essentially indicates that the 'true' 
percentage of pink salmon that are infested with <I>L. salmonis</I> is highly likely to lie somewhere in the range between 1.7% and 3.7%. In general, the smaller 
the confidence interval the more likely it is that the point estimate - in our case for prevalence of lice - is close to the true value.  [For those who have 
an interest in such things, the technically correct interpretation of a 95% CI is that if we were to go out and sample from pink salmon populations 100 times, 
then in 95 of these cases the true value would lie within the specified confidence intervals.]</P>
 
<P><B>Q: why does the BAMP protocol not simply count lice on live wild fish?</B></P>
 
<P>Since 2011 the protocol for assessing sea lice loads on wild Pacific salmonids has involved lethal sampling in the field, followed by a detailed analysis 
of lice and fish at one of two laboratories. Over the years alternative approaches have been used. Some researchers have used ‘live licing’ to assess 
the sea lice loads on wild salmon. Indeed, in 2010 the BAMP monitoring program used both live and lethal sampling to assess lice loads to gain an 
understanding of 'infection pressure'. As of 2011 the Science Team moved to 
ensure the highest quality and consistency of lice monitoring on wild salmon by assessing samples in the laboratory. This becomes particularly important 
when prevalence levels are very low – for example, on one trip at the start of the 2011 season only 8 fish from over 600 samples were found to be infected. 
In addition, when lice are in their early stages of development, as they often are on small fish, it is almost impossible to distinguish between the species 
of lice involved without using a microscope in the laboratory. Knowing whether the infestation is by <I>L. salmonis</I> as opposed to one of the <I>Caligus</I> 
species is an important factor in clearly understanding the interactions between lice on wild and farmed fish.</P>
 
<P><B>Q: what mechanism(s) are available within the BAMP research process to alter details of the field and/or laboratory protocols?</B></P>
 
<P>Each year the BAMP Science Team meets to review activities over the preceding year and to plan for both the research and field activities 
in the succeeding year. This is the main point at which alterations to overall plans and protocols are made. A key reason for this is that a 
face-to-face meeting is a more conducive environment to consider complex arguments and ensure that all viewpoints are adequately explored. 
However it is also seen as important that once the sampling season has begun the protocols remain consistent for the rest of that year. Of 
course if critical issues are raised in the course of carrying out project activities the Science Team can address these specifically in 
the interim. For example, in 2010 it was brought to the attention of the BAMP Science Team that the process of euthanizing sampled fish did 
not fully conform to welfare standards that many would consider essential. This important issue was dealt with immediately and a modified protocol 
adopted from the succeeding field trip forward.</P>


