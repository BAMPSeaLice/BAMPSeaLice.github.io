<H2>articles and media</H2>
<P>The public face of BAMP is launching in early November 2011 so as yet there are few media reports or articles to list. As the research activities of BAMP progress and results are reported
and commented on, this area of the web site will be used to capture links to these reports.

In the meantime a short op-ed style piece written in April 2011 by the BAMP Science Team Manager outlining the background to BAMP is reproduced below.
</P>
<H3>Fishy Bedfellows (Crawford Revie*)</H3>
<P>While the debate about salmon farming continues to rage in the media and at water coolers across BC, something very unusual has been happening behind the scenes.
</P>
<P>Quietly and without fanfare, an utterly unprecedented collaborative research project has begun; a research project involving some very strange and unexpected bedfellows. 
Initiated by the biggest salmon farming company in the world, Marine Harvest, and the five environmental groups comprising the Coastal Alliance for Aquaculture Reform (CAAR), the 
Broughton Area Monitoring Plan has brought together co-sponsors and scientists from Fisheries and Oceans Canada, the three largest salmon farming operators in BC (Marine Harvest Canada, 
Mainstream Canada and Grieg Seafood) and academic researcher Dr. Martin Krkosek. As a professor of Epidemiology with the University of Prince Edward Island, I have been contracted by the 
parties to guide and oversee the program.
</P>
<P>After months of negotiation, all parties have signed a formal data-sharing agreement and in the spring of 2010 began a collaborative field monitoring and research project. 
The work is designed to assess sea lice levels on juvenile wild salmon in the Broughton Archipelago, evaluate the effectiveness of farm management measures, analyze historical scientific data from 
all sources and eventually use modelling to better predict and manage sea lice transmission.
</P>
<P>It may sound like just another wonky science project to the lay person, but to anyone embroiled in the “my science/your science” debate raging for years around net-cage farming, sea lice infestation, 
data access and the sustainability of wild salmon, the Broughton Area Monitoring Plan or “BAMP” is a huge breakthrough.
</P>
<P>As the BAMP moves forward, Mainstream, Grieg and Marine Harvest are sharing their on-farm data. Fisheries and Oceans Canada scientists are contributing their extensive information resources. 
Dr. Krkosek is subjecting his modelling and analysis to scrutiny by the BAMP team. Representatives of CAAR are joining salmon farm employees on field teams. All parties are actively engaging 
and cooperating to devise the most rigorous and sound methods for sampling wild salmon, assessing lice levels, analyzing the possible links to on-farm lice outbreaks and evaluating the 
effectiveness of treatment and management measures.
</P>
<P>The scientists have been working hard to ensure both accuracy and unbiased fairness within the program. Methods and locations to undertake lice sampling were agreed prior to the start of 
monitoring the juvenile wild salmon out-migration and incorporated the best of both DFO’s and Dr. Krkosek’s previous methodologies. In 2010, wild salmon samples taken in the field were randomly 
divided in half and sent to two different laboratories for assessment. Quality assurance protocols were developed for the labs and agreed by all parties. Assessment data were audited on a real 
time basis. Field teams were a mixture of researchers affiliated with the salmon farming companies, DFO and CAAR member groups.
</P>
<P>The next step is building a shared data base that will incorporate recent and historic wild and farmed salmon data. Then the team will begin a complex series of analyses and modeling, 
assessing trends and patterns between areas within the Broughton Archipelago and through time. Eventually, this unprecedented undertaking should lead to some shared, published conclusions 
and recommendations around steps that may be taken to mitigate or eliminate risks to B.C.’s wild juvenile salmon.
</P>
<P>
For anyone outside the process, science can seem frustratingly slow. We all want answers to the challenges facing our oceans, our communities and our businesses; and we want them now. 
The BAMP - this collaborative research effort to bring the best minds and most extensive data sets together in a single initiative - provides an unprecedented opportunity to find answers that all 
concerned parties can and will accept as unbiased, unvarnished and meaningful.
</P>
