<H2>objectives</H2>
<P>Key objectives of the BAMP program are:
	<ul>
			<li>Conduct juvenile wild salmon sampling (on around 100 sites) in the Broughton Archipelago during their spring migration, 
				and laboratory analyses of samples to improve our understanding of sea lice levels on juvenile wild pink and chum salmon in the Broughton Archipelago;</li>
			<li>Integrate historical scientific data for the Broughton Archipelago area (government, researchers, farming industry)
				 with recent BAMP data (2010 onwards) to research interactions between sea lice, wild salmon and farmed salmon in the broader geographic area;</li>
			<li>Evaluate effectiveness of different farm management approaches to reduce the potential for lice from farmed salmon to infect juvenile pink and chum salmon;</li>
			<li>Use models to better predict sea lice transmission and assess the efficacy of different farm and
				 lice management strategies to reduce risk to juvenile pink and chum salmon during the spring migration;</li>
			<li>Compare trends and patterns between areas within the Broughton Archipelago and through time; and</li>
			<li>Determine if sea lice infection negatively affects marine survival of juvenile pink and chum salmon in the Broughton Archipelago and also include other
				 relevant risk factors such as host age, predators, the presence of other diseases or infectious agents and environmental variables such as salinity, and geography.</li>
	</ul>
</P>

