<H2>BAMP 2012 field/laboratory activities and preliminary findings</H2>
<P>
	<UL>
		<LI>In 2012 the BAMP programme conduct three sampling trips (in April, May and June), thus covering a slightly less 
			extensive period than the four trips carried out in the 2010 and 2011 outmigration periods (March - June). In 
			addition, only around 80 sites were targeted during each trip (in comparison to just over 100 in the previous two years). 
			However, the number of wild salmon captured per trip was similar to that seen in 2010 and 2011 as the sites excluded 
			were those which had rarely produced samples.</LI>
		<LI>In total just over 40,000 fish (31,057 pink; 9,128 chum and 398 coho) were captured.</LI>
		<LI>Where available, up to 30 fish per species was retained at each sampling location. 
			This resulted in a sub-set of around 7,000 fish (~52% pink, ~44% chum and ~4% coho) which were lethally sampled 
			and submitted to one of two laboratories for subsequent analysis.</LI>
		<LI>Despite the reduction in number of trips and sampling locations in 2012, there were actually slightly more wild 
			salmon retained for analysis in the laboratories than in either 2010 or 2011. This was primarily due to the higher 
			proportion of chum present in 2012, with 3042 individuals retained for analysis in comparison to 1398 and 1534 in 
			2010 and 2011 respectively.</LI>
		<LI>As noted in previous years, a strong correlation existed between the prevalence and abundance levels of sea lice on wild salmon.</LI>
		<LI>In general the prevalence of sea lice was similar to that seen in 2010, i.e. did not reflect the very low levels seen in 2011 
			though Trip 1 (April) was quite similar to the same month last year.</LI>
		<LI>The temporal trends were similar to those observed in previous years with the lowest levels seen in April (5-15%), 
			peaking in May (14-28%), before dropping off in June (3-9%). In contrast to 2001, the <I>C. clemensi</I> species was not so obviously 
			dominant, and in June there were actually significantly more <I>L. salmonis</I> lice on both pink and chum salmon.</LI>
		<LI>As was also noted in 2010 and 2011, the levels of infestation on chum salmon was consistently higher (between 2-10%) 
			across all trips and for both species of sea lice.</LI>
		<LI>The coho salmon assessed did appear to have higher levels of sea lice prevalence and this was particularly noticeable 
			for <I>C. clemensi</I> in Trip 1 (55%) and Trip 2 (33%). However, these values were based on small samples sizes making it difficult 
			to generalize and/or make definitive comments on temporal trends; coho have not represented more than 5% of samples in any of the past 3 years.</LI>
		<LI>In terms of lice stages, chalimus/non-motile lice accounted for almost 100% of infestation in the first trip, dropping to 
			74% and 56% on the second trip for chum and pink salmon respectively. During the third trip mobile stages began to dominate, 
			accounting for 64% of infestation on chum and 86% of all lice found on pink salmon. These trends were similar to those observed in earlier years.</LI>
		<LI>Measurements made on the fish assessed in the laboratories indicated that pink and chum exhibited similar growth patterns as were seen in 
			2010 and 2011. As there was not a trip in March the lower end of the size range was not sampled with pink salmon growing from 0.3g in April 
			to just over 3g by the end of June. Chum grew from 0.6g to almost 4.9g in June (somewhat larger by the end of the season than the 4.2g seen 
			in 2010 and 2011).</LI>
		<LI>As would be expected, given the higher lice prevalence values, the proportion of fish affected by scarring in 2012 was higher than that 
			seen in 2011, however with mean levels of 3.4% and 3% for chum and pink respectively this was lower than the levels of scarring observed in 2010.</LI>
	</UL>
</P>

